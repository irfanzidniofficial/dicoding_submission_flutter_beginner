class PropertyModel {
  final String name;
  final String price;
  final String city;
  final String address;
  final String type;
  final String instalment;
  final String desc;
  final String developer;
  final String image;
  final int bedroom;
  final int bathroom;
  final int area;

  PropertyModel({
    required this.name,
    required this.price,
    required this.city,
    required this.address,
    required this.type,
    required this.instalment,
    required this.desc,
    required this.developer,
    required this.image,
    required this.bedroom,
    required this.bathroom,
    required this.area,
  });
}
